#include <stdlib.h>
#include <stdio.h>
#include <string.h>
#define ADD(x, y) (x + y)
#define CUBEX(x) ((x) * (x) * (x))
#define REMAINDER_DIVIDED_BY_4(x) ((x) % 4)
#define CHECK_BIGGER_100(x, y) ((x) * (y) > 100 ? 1 : 0)
#define NELEMS(a) sizeof(a)

int main(int argc, char *argv[]) {
    printf("ADD(2, 5): %d\n", ADD(2,5));
    printf("CUBEX(3): %d\n", CUBEX(3));
    printf("REMAINDER_DIVIDED_BY_4(7): %d\n", REMAINDER_DIVIDED_BY_4(7));
    printf("CHECK_BIGGER_100(4, 25): %d\n", CHECK_BIGGER_100(4, 25));
    printf("CHECK_BIGGER_100(5, 25): %d\n", CHECK_BIGGER_100(5, 25));
    char array[5] = {'1', '2', '3', '4', '5'};
    printf("Array lenght: %d", NELEMS(array));
}
